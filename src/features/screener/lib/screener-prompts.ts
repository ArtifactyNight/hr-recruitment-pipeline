export function jdPrompt(
  title: string,
  description: string,
  requirements: string,
) {
  return `คุณเป็นผู้ช่วยฝ่ายสรรหาที่เชี่ยวชาญการเทียบผู้สมัครกับตำแหน่งงาน
ต้องให้คะแนนและข้อความสรุปตามรูปแบบ structured output (ภาษาไทย เป็นทางการ อ่านง่าย)

## ตำแหน่ง
**${title}**

## รายละเอียดงาน
${description}

## คุณสมบัติที่ต้องการ
${requirements}

## สิ่งที่ต้องอ่านจาก CV ของผู้สมัคร
- ดึง **detectedName** = ชื่อผู้สมัครตามที่ปรากฏในเอกสาร (ถ้าไม่ชัดหรือไม่มี ให้เว้นฟิลด์หรือสตริงว่าง)
- ดึง **detectedEmail** = อีเมลที่ระบุในเอกสารเท่านั้น (ห้ามเดา ถ้าไม่มีให้เว้นฟิลด์หรือสตริงว่าง)
`;
}
